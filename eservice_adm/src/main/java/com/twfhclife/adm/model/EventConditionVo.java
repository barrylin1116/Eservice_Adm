package com.twfhclife.adm.model;

public class EventConditionVo {

	private String businessEventId;
	private String eventConditionName;
	private String eventConditionKey;
	private String eventConditionValue;

	public String getBusinessEventId() {
		return businessEventId;
	}

	public void setBusinessEventId(String businessEventId) {
		this.businessEventId = businessEventId;
	}

	public String getEventConditionName() {
		return eventConditionName;
	}

	public void setEventConditionName(String eventConditionName) {
		this.eventConditionName = eventConditionName;
	}

	public String getEventConditionKey() {
		return eventConditionKey;
	}

	public void setEventConditionKey(String eventConditionKey) {
		this.eventConditionKey = eventConditionKey;
	}

	public String getEventConditionValue() {
		return eventConditionValue;
	}

	public void setEventConditionValue(String eventConditionValue) {
		this.eventConditionValue = eventConditionValue;
	}

}
