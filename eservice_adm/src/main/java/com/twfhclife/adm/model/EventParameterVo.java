package com.twfhclife.adm.model;

public class EventParameterVo {

	private String businessEventId;
	private String eventParameterName;
	private String eventParameterCode;
	private String eventParameterValue;

	public String getBusinessEventId() {
		return businessEventId;
	}

	public void setBusinessEventId(String businessEventId) {
		this.businessEventId = businessEventId;
	}

	public String getEventParameterName() {
		return eventParameterName;
	}

	public void setEventParameterName(String eventParameterName) {
		this.eventParameterName = eventParameterName;
	}

	public String getEventParameterCode() {
		return eventParameterCode;
	}

	public void setEventParameterCode(String eventParameterCode) {
		this.eventParameterCode = eventParameterCode;
	}

	public String getEventParameterValue() {
		return eventParameterValue;
	}

	public void setEventParameterValue(String eventParameterValue) {
		this.eventParameterValue = eventParameterValue;
	}

}
